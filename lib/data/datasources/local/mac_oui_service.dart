  Future<void> loadDatabase() async {
    if (_isLoaded) return;
    
    try {
      // Load from asset file if available
      final jsonString = await rootBundle.loadString('assets/oui_database.json');
      final json = jsonDecode(jsonString) as Map<String, dynamic>;
      _ouiDatabase = json.map((k, v) => MapEntry(k.toUpperCase(), v.toString()));
    } catch (e) {
      // Use built-in database
      _ouiDatabase = Map.from(_commonOuiMappings);
    }
    
    _isLoaded = true;
  }
  
  String lookupManufacturer(String macAddress) {
    if (!_isLoaded) {
      loadDatabase();
    }
    
    final oui = macAddress.extractOui.toUpperCase();
    if (oui.isEmpty) return 'Unknown';
    
    return _ouiDatabase[oui] ?? 'Unknown';
  }
  
  String detectDeviceType(String manufacturer, String? hostname) {
    final lowerMan = manufacturer.toLowerCase();
    final lowerHost = (hostname ?? '').toLowerCase();
    
    // Check hostname first
    if (lowerHost.contains('phone') || lowerHost.contains('mobile')) {
      return 'phone';
    }
    if (lowerHost.contains('tablet') || lowerHost.contains('ipad')) {
      return 'tablet';
    }
    if (lowerHost.contains('laptop') || lowerHost.contains('notebook')) {
      return 'laptop';
    }
    if (lowerHost.contains('desktop') || lowerHost.contains('pc')) {
      return 'desktop';
    }
    if (lowerHost.contains('tv') || lowerHost.contains('smarttv')) {
      return 'tv';
    }
    if (lowerHost.contains('game') || lowerHost.contains('playstation') || 
        lowerHost.contains('xbox') || lowerHost.contains('nintendo')) {
      return 'gaming';
    }
    if (lowerHost.contains('printer') || lowerHost.contains('scanner')) {
      return 'printer';
    }
    if (lowerHost.contains('camera') || lowerHost.contains('cam')) {
      return 'camera';
    }
    if (lowerHost.contains('router') || lowerHost.contains('accesspoint') || 
        lowerHost.contains('ap')) {
      return 'router';
    }
    
    // Check manufacturer
    if (lowerMan.contains('apple') && (lowerHost.contains('iphone') || 
        lowerHost.contains('phone'))) {
      return 'phone';
    }
    if (lowerMan.contains('apple') && lowerHost.contains('ipad')) {
      return 'tablet';
    }
    if (lowerMan.contains('samsung') && (lowerHost.contains('phone') || 
        lowerHost.contains('mobile'))) {
      return 'phone';
    }
    if (lowerMan.contains('huawei') || lowerMan.contains('xiaomi') || 
        lowerMan.contains('oppo') || lowerMan.contains('vivo') || 
        lowerMan.contains('oneplus') || lowerMan.contains('realme')) {
      return 'phone';
    }
    if (lowerMan.contains('espressif') || lowerMan.contains('esp') || 
        lowerMan.contains('arduino') || lowerMan.contains('raspberry')) {
      return 'iot';
    }
    if (lowerMan.contains('microsoft') || lowerMan.contains('dell') || 
        lowerMan.contains('hp') || lowerMan.contains('lenovo') || 
        lowerMan.contains('asus') || lowerMan.contains('acer')) {
      return 'laptop';
    }
    
    return 'unknown';
  }
